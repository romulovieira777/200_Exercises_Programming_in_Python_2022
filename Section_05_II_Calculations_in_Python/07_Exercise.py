"""
Exercise No. 07

Calculate the midpoint of the segment with ends at the points: A = (2, 4), B = (-4, 6) and print the result to the
console as show below.

Expected result:

    The middle point: (-1.0, 5.0)
"""
a = (2, 4)
b = (-4, 6)

x = (a[0] + b[0]) / 2
y = (a[1] + b[1]) / 2

print(f'The middle point: ({x}, {y})')
